#!/bin/zsh

# Скрипт для отправки синхронного сообщения Еве через прямое исполнение скрипта навыка ask_eva

# Проверка, был ли передан аргумент (сообщение)
if [ -z "$1" ]; then
  echo "Использование: $0 \"<сообщение для Евы>\""
  exit 1
fi

# Сообщение для отправки - это первый аргумент командной строки
MESSAGE="$1"

# Прямой вызов скрипта, который использует навык ask_eva, как описано в его SKILL.md
/opt/homebrew/lib/node_modules/openclaw/skills/ask_eva/scripts/run_eva.sh "$MESSAGE"
